                                                    Asociația Creștem România Împreună şi să mă angajez în
                                                    activităţile
                                                    ce ţin de natura activităţii de voluntariat. Înţeleg că
                                                    activităţile
                                                    s-ar putea să includă, fără a se limita la activități de
                                                    plantare
                                                    sau ecologizare, construcţia şi reabilitarea spațiilor care
                                                    deservesc diverse comunități, sesiuni de training, organizarea
                                                    de
                                                    evenimente (tabere, conferinte, teambuilding), evenimente ce
                                                    însumează toate proiectele asociației.<br/>


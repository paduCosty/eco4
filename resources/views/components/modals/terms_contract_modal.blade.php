                                                            <strong>Renunţare.</strong> Voluntarul declară că
                                                            renunţă la
                                                            orice pretenţie împotriva Asociației şi succesorilor
                                                            acestuia în legătură cu orice dispută, controversă sau
                                                            pretenţie, materială sau nu, care ar putea rezulta din
                                                            sau
                                                            în legătură cu Activităţile efectuate.<br>

                                                            domeniu.<br>
                                                            Voluntarul, prin aceasta, îşi asumă în mod expres şi
                                                            irevocabil riscul de rănire sau vătămare în cadrul
                                                            activităţilor efectuate şi eliberează Asociația Creștem
                                                            România Împreună de orice răspundere în caz de vătămare
                                                            corporală, boală, deces sau distrugere a proprietăţii
                                                            cauzate de efectuarea activităţilor.
                                                        </li>
                                                        <li><strong>Asigurare.</strong> Voluntarul înţelege, cu
                                                            excepţia
                                                            cazului în care s-a convenit altfel în scris de către
                                                            Asociația Creștem România Împreună, că Asociația Creștem
                                                            România Împreună nu suportă asigurarea de sănătate,
                                                            medicală
                                                            sau de invaliditate pentru nici un Voluntar.<br>
                                                            Este recomandabil ca fiecare Voluntar să aibă propria
                                                            asigurare medicală sau de sănătate.
                                                        </li>


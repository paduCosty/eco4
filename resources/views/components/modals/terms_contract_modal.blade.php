                                                            eliberează Asociația Creștem România Împreună de orice
                                                            pretenţie pe care o poate avea faţă de Asociația Creștem
                                                            România Împreună cu privire la orice vătămări corporale,
                                                            personale, boală, moarte sau daune aduse proprietăţii
                                                            care
                                                            pot sa apară în urma Activităţilor efectuate. Voluntarul
                                                            înţelege şi este de acord că Asociația Creștem România
                                                            Împreună nu îşi asumă nici o responsabilitate sau
                                                            obligaţie
                                                            de a asigura asistenţa financiară sau alt tip de
                                                            asistenţă
                                                            Voluntarului, incluzând, dar fără a se limita la,
                                                            asigurare
                                                            medicală, de sănătate sau invaliditate, suportarea
                                                            cheltuielilor de spitalizare sau recuperare, în caz de
                                                            rănire sau boală.
                                                        </li>

                                                        <li><strong>Tratamentul medical.</strong> Voluntarul, prin
                                                            prezenta, eliberează Asociația Creștem România Împreună

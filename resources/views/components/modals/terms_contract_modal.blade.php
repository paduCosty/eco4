                                                    <strong>Contract de VOLUNTARIAT conform legii 78/2014 privind
                                                        reglementarea activităţii de voluntariat din România,
                                                        încheiat
                                                        valabil</strong><br/><br/>
                                                    <strong>A. PARTILE CONTRACTANTE:</strong><br/><br/>

                                                    Asociatia Creștem România Împreună, cu sediul în Săcele, str.
                                                    Ciucaș
                                                    nr. 54, jud. Brașov, CIF 34521305, si cont bancar deschis la
                                                    Credit
                                                    Europe Bank, reprezentata de Mihai Mihu, în calitate de
                                                    președinte,
                                                    numită în continuare Organizație gazdă sau ASOCIAȚIA, si
                                                    VOLUNTARUL.<br/><br/>

                                                    <strong>B. OBIECTUL CONTRACTULUI:</strong><br/><br/>
                                                    Obiectul prezentului contract il constituie desfasurarea de
                                                    catre
                                                    voluntar a activitatilor prevazute in fisa postului, anexata,
                                                    fara a
                                                    obtine o contraprestatie materiala.<br/>
                                                    <strong><br/>


                                                        C. DREPTURILE SI OBLIGATIILE
                                                        VOLUNTARULUI:</strong><br/><br/>
                                                    <strong>Drepturi:</strong><br/>

                                                    <ul>
                                                        <li>de a fi tratat ca si coleg cu drepturi egale de catre
                                                            intreg
                                                            staful beneficiarului voluntariatului;
                                                        </li>
                                                        <li>de a participa activ la elaboarea si derularea
                                                            programelor
                                                            in considerarea carora se incheie prezentul contract;
                                                        </li>
                                                        <li>de a i se asigura un loc unde sa-si desfasoare
                                                            activitatea
                                                            si accesul la echipamentul si consumabilele necesare
                                                            derularii activitatii;
                                                        </li>
                                                        <li>de a i se asigura de catre beneficiarul voluntariatului,
                                                            protectia muncii, in functie de natura si de
                                                            caracteristicile activitatii pe care o desfasoara;
                                                        </li>
                                                        <li>de a i se elibera de catre beneficiarul voluntariatului,
                                                            certificatul nominal care atesta calitatea de voluntar;
                                                        </li>
                                                        <li>de a i se rambursa cheltuielile efectuate in timpul si
                                                            in
                                                            legatura cu activitatile desfasurate de voluntar si care
                                                            fac
                                                            obiectul prezentului contract, prestabilite de catre
                                                            beneficiarul voluntariatului, conform procedurii interne
                                                            a
                                                            Asociației;
                                                        </li>
                                                        <li>de a beneficia de titluri onorifice, decoratii, premii,
                                                            in
                                                            conditiile legii;
                                                        </li>
                                                        <li>de a beneficia de dreptul la confidenţialitate şi
                                                            protecţia
                                                            datelor personale;
                                                        </li>
                                                    </ul>

                                                    <strong>Obligatii:</strong><br/>
                                                    <ul>
                                                        <li>sa indeplineasca la timp sarcinile primite din partea
                                                            beneficiarului voluntariatului;
                                                        </li>
                                                        <li>sa anunte din timp orice schimbare survenita in
                                                            derularea
                                                            programului (inclusiv intârzieri, absente, etc.);
                                                        </li>
                                                        <li>sa prezinte supervizorului rapoartele de activitate;
                                                        </li>
                                                        <li>sa pastreze confidentialitatea informatiilor la care are
                                                            acces in cadrul activitatilor de voluntariat, inclusiv
                                                            dupa
                                                            incetarea activitatii;
                                                        </li>
                                                        <li>sa cunoasca misiunea si activitatile beneficiarului
                                                            voluntariatului;
                                                        </li>
                                                        <li>sa ocroteasca bunurile pe care le foloseste in cadrul
                                                            activitatii de voluntariat;
                                                        </li>
                                                        <li>sa aiba un comportament adecvat;</li>
                                                        <li>sa cunoasca si sa respecte procedurile, politicile si
                                                            regulamentele interne ale beneficiarului
                                                            voluntariatului.
                                                        </li>
                                                    </ul>

                                                    <br/>

                                                    <strong>D. DREPTURILE SI OBLIGATIILE BENEFICIARULUI
                                                        VOLUNTARIATULUI
                                                        SI/SAU ALE ASOCIATIEI:</strong><br/><br/>
                                                    <strong>Drepturi:</strong><br/>

                                                    <ul>
                                                        <li>de a exclude voluntarul din programele si proiectele in
                                                            care
                                                            desfasoara activitati de voluntariat, daca acesta nu
                                                            respecta prevederile prezentului contract;
                                                        </li>
                                                        <li>de a detine toate drepturile asupra materialelor,
                                                            rapoartelor, informatiilor, documentatiei si a altor
                                                            materiale scrise, create, colectate sau/si produse de
                                                            voluntar in perioada desfasurarii activitatilor care fac
                                                            obiectul prezentului contract de voluntariat;
                                                        </li>
                                                        <li>de a verifica si a superviza activitatea voluntarului;
                                                        </li>
                                                        <li>de a cere voluntarului rapoarte de activitate;</li>
                                                        <li>de a cere informatii si recomandari despre voluntar,
                                                            altor
                                                            persoane.
                                                        </li>
                                                    </ul>

                                                    <strong>Obligatii:</strong><br/>

                                                    <ul>
                                                        <li>sa-l trateze pe voluntar ca si coleg cu drepturi
                                                            egale;
                                                        </li>
                                                        <li>sa asigure voluntarului un loc unde sa-si desfasoare
                                                            activitatea si accesul la echipamentul si consumabilele
                                                            necesare derularii activitatii;
                                                        </li>
                                                        <li>sa asigure conditii legale de protectie a muncii in
                                                            functie
                                                            de natura si caracteristicile activitatilor desfasurate
                                                            de
                                                            voluntar;
                                                        </li>
                                                        <li>sa asigure voluntarul, in conditiile prevazute de lege,
                                                            impotriva riscurilor de accident si de boala ce decurg
                                                            din
                                                            natura activitatilor desfasurate; in lipsa asigurarii,
                                                            costul prestatiilor medicale se suporta integral de
                                                            catre
                                                            beneficiarul voluntariatului;
                                                        </li>
                                                        <li>sa ramburseze voluntarului cheltuielile efectuate de
                                                            acesta
                                                            in timpul si in legatura cu activitatile ce fac obiectul
                                                            prezentului contract de voluntariat, prestabilite;
                                                        </li>
                                                        <li>sa elibereze voluntarului certificatul nominal care
                                                            atesta
                                                            calitatea de voluntar.
                                                        </li>
                                                    </ul>

                                                    <br/>

                                                    <strong>E. DURATA CONTRACTULUI:</strong><br/><br/>
                                                    Prezentul contract se incheie pentru o perioada de 12
                                                    (doisprezece
                                                    luni) , cu posibilitatea innoirii sale automate, daca nici una
                                                    dintre parti nu solicita incetarea acestuia.<br>
                                                    Programul si durata timpului de lucru sunt stabilite prin fisa
                                                    postului, anexata.<br/><br/>

                                                    <strong>F. RASPUNDEREA CONTRACTUALA:</strong><br/><br/>
                                                    Neexecutarea sau executarea necorespunzatoare a obligatiilor
                                                    asumate
                                                    prin prezentul contract atrage raspunderea partii in culpa,
                                                    fiind
                                                    supusa regulilor prevazute de Codul Civil.<br/><br/>
                                                    <strong>G. MODIFICAREA, COMPLETAREA, RENEGOCIEREA
                                                        CONTRACTULUI:</strong><br/><br/>
                                                    Modificarea sau completarea prezentului contract poate fi facuta
                                                    prin acordul partilor, prin act aditional.<br>
                                                    Daca pe parcursul executarii prezentului contract intervine,
                                                    independent de vointa partilor, o situatie de natura sa
                                                    ingreuneze
                                                    executarea obligatiilor ce revin voluntarului, contractul va fi
                                                    renegociat.<br>
                                                    Renegocierea contractului de voluntariat se va face la cererea
                                                    scrisa a uneia dintre parti, formulata in termen de 15 zile de
                                                    la
                                                    data la care a intervenit respectiva situatie.<br/><br/>

                                                    <strong>H. INCETAREA CONTRACTULUI:</strong><br/><br/>
                                                    Prezentului contract inceteaza in urmatoarele situatii:<br/>

                                                    <ul>
                                                        <li>la expirarea termenului pentru care este incheiat;</li>
                                                        <li>prin acordul partilor;</li>
                                                        <li>denuntarea unilaterala din initiativa uneia dintre
                                                            parti, cu
                                                            un preaviz de 15 zile, fara obligatia prezentarii
                                                            motivelor;
                                                        </li>
                                                        <li>daca situatia descrisa in art.7 alin. (2) face
                                                            imposibila
                                                            executarea in continuare a contractului, acesta este
                                                            reziliat de plin drept.
                                                        </li>
                                                    </ul>

                                                    <br/>

                                                    <strong>J. LITIGII:</strong><br/><br/>
                                                    Litigiile izvorâte din incheierea, modificarea, executarea sau
                                                    incetarea prezentului contract sunt de competenta instantelor
                                                    judecatoresti, daca partile contractante nu le rezolva pe cale
                                                    amiabila.<br>
                                                    Prezentul contract s-a incheiat in doua exemplare, fiecare având
                                                    aceeasi valoare juridica.<br/><br/>

                                                    BENEFICIAR AL VOLUNTARIATULUI, VOLUNTAR,


                                                    FIŞA VOLUNTARULUI<br/><br/>
                                                    Denumirea postului: VOLUNTAR PLANTEAZĂ ÎN ROMÂNIA / ASOCIAȚIA
                                                    CREȘTEM ROMÂNIA ÎMPREUNĂ<br/><br/>


                                                    Programul de voluntariat: participare la workshop-urile de
                                                    plantare
                                                    și/sau ecologizare Plantează în România<br/><br/>

                                                    <strong> DESCRIEREA POSTULUI:</strong><br/>

                                                    <strong>Scopul:</strong><br/>

                                                    Participarea la workshop-urile de plantare si/sau
                                                    ecologizare<br/>

                                                    Responsabilități (se vor enumera în funcție de numărul total de
                                                    sarcini presupus de post) plantarea unui numar de minim 5 puieti
                                                    in
                                                    ziua actiunii<br/><br/>


                                                    <strong>RELAŢII DE SUBORDONARE ÎN CADRUL ACTIVITĂȚII DE
                                                        VOLUNTARIAT</strong><br/>

                                                    Se subordonează direct: coordonatorului de actiune sau
                                                    judetean/al
                                                    municipiului Bucuresti/Ilfov<br/><br/>

                                                    <strong>CONDIŢII DE DESFĂȘURARE A ACTIVITĂȚII DE
                                                        VOLUNTARIAT</strong><br/>

                                                    <strong>Programul/orarul:</strong> stabilit in functie de
                                                    actiunea
                                                    de voluntariat aleasa<br/>
                                                    <strong>Locul de desfăşurare:</strong> pe teren, in zonele
                                                    alocate<br/><br/>


                                                    <strong>SPECIFICAREA POSTULUI</strong><br/>
                                                    <strong>Cunoştinţe şi abilităţi necesare:</strong><br/>
                                                    <ul>
                                                        <li>proactivitate</li>
                                                        <li>dinamism</li>
                                                        <li>spirit de echipa</li>
                                                    </ul>
                                                    <strong>CONDIȚII DE PARTICIPARE</strong><br/>
                                                    descrise pe pagina proiectului Plantează în România


